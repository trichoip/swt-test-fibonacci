package com.swt301.testng.user;

/**
 *
 * @author minh tri
 */
public class UserDTO {

    private String userID;
    private String fullName;
    private String password;
    private String roleID;
    private boolean status;

    public UserDTO() {

    }

    public UserDTO(String userID, String fullName, String password, String roleID, boolean status) {
        this.userID = userID;
        this.fullName = fullName;
        this.password = password;
        this.roleID = roleID;
        this.status = status;
    }

    public UserDTO(String userID, String fullName) {
        this.userID = userID;
        this.fullName = fullName;
    }

    public String getUserID() {
        return userID;
    }

    public void setUserID(String userID) {
        this.userID = userID;
    }

    public String getFullName() {
        return fullName;
    }

    public void setFullName(String fullName) {
        this.fullName = fullName;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getRoleID() {
        return roleID;
    }

    public void setRoleID(String roleID) {
        this.roleID = roleID;
    }

    public boolean isStatus() {
        return status;
    }

    public void setStatus(boolean status) {
        this.status = status;
    }

    @Override
    public String toString() {
        return "[" + userID + ", " + fullName + ", " + password + ", " + roleID
                + ", " + status + "]";
    }

}
